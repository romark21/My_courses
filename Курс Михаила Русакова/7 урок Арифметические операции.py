x = 40
y = 20
print("x =", x)
print("y =", y)

#Основные математические операции
print("x + y =", x + y)
print("x - y =", x - y)
print("x * y =", x * y)
print("x / y =", x / y)
print("x % y =", x % y) #Деление с остатком
print("x // y =", x // y)
print("x ** y =", x ** y) #Возведение в степень
print("(2 + 3 + 7) * 2 =", (2 + 3 + 7) * 2)

#Битовые операции
x = 5
y = 7
print("x =", x, "=", bin(x))
print("y =", y, "=", bin(y))
print("x =", x, "=", hex(x))
print("---------")
print("x =", x, "=", bin(x))
print("y =", y, "=", bin(y))
print("x | y =", bin(x | y))
print("x & y =", bin(x & y))
print("x ^ y =", bin(x ^ y))
